#include "libft.h"

int		c_words(char const *s, char ch)
{
	int count;

	count = 0;
	while (*s)
	{
		if (*s != ch)
		{
			while (*s != ch && *s)
				s++;
			count++;
		}
		s++;
	}
	return (count);
}
