#include "libft.h"

int		if_int(const char *s1, const char *s2, size_t i, size_t len)
{
	int c;

	c = 0;
	while (i < len && s1[i] && s2[c])
	{
		if (s1[i] != s2[c])
			return (0);
		i++;
		c++;
	}
	return (s2[c] == '\0');
}
